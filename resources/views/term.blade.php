                    <b>บริษัท วงษ์พาณิชย์ รีไซเคิล ระยอง จำกัด</b> (“บริษัท”) ตระหนักถึงความสำคัญของข้อมูลส่วนบุคคลและข้อมูลอื่น (รวมเรียกว่า “ข้อมูล”) อันเกี่ยวกับลูกค้าของบริษัท 
