                      (“รวมเรียกว่า “ประมวลผล”) ซึ่งดำเนินการโดยบริษัท รวมถึงบุคคลที่เกี่ยวข้องในนามของบริษัท โดยมีเนื้อหาสาระดังต่อไปนี้</p>
